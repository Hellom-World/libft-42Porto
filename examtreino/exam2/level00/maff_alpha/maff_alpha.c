#include <unistd.h>

int	main(int ac, char **av)
{	
	(void)ac;
	(void)av;
	char	c1;
	char	c2;
	
	c1 = 'a';
	c2 = 'B';
	while( c2 <= 'Z')
	{
		write(1, &c1, 1);
		write(1, &c2, 1);
		c1 = c1 + 2;
		c2 = c2 + 2;
	}
	return (0);
}
